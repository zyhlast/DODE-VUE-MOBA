      <el-form-item label="英雄名称">
        <el-input v-model="model.name"></el-input>
      </el-form-item>
      <el-form-item label="英雄头像">
        <el-upload class="avatar-uploader"
                   :action="$http.defaults.baseURL + '/upload'"
                   :show-file-list="false"
                   :on-success="afterUpload">
          <img v-if="model.avatar"
               :src="model.avatar"
               class="avatar">
          <i v-else
             class="el-icon-plus avatar-uploader-icon"></i>
        </el-upload>
      </el-form-item>

      <el-form-item>
        <el-button type="primary"
                   native-type="submit">保存</el-button>
      </el-form-item>
    </el-form>
  </div>
</template>
<script>
export default {
  props: {
    id: {}
  },
  data () {
    return {
      model: {},
      parents: []
    }
  },
  methods: {
    afterUpload (res) {      console.info(res)
      this.$set(this.model, 'avatar', res.url)
    },
    async save () {
      let res
      if (this.id) {
        res = await this.$http.put(`rest/Hero/${this.id}`, this.model)
      } else {
        res = await this.$http.post('rest/Hero', this.model)
      }
      this.$router.push('/heroes/list')
      this.$message({
        type: 'success',
        message: '保存成功'
      })
      console.info(res)
    },
    async fetch () {
      const res = await this.$http.get(`rest/Hero/${this.id}`)
      this.model = res.data
    },
    // async fetchParents () {
    //   const res = await this.$http.get(`rest/Hero`)
    //   this.parents = res.data
    // }

  },
  created () {
    // this.fetchParents()
    this.id && this.fetch()
  },
}
</script>
