<template>
  <div class="about">
    <h1>{{id?"编辑":"新建"}}英雄</h1>
    <el-form label-width="120px"
             @submit.native.prevent="save">
      <!-- <el-form-Hero label="上级分类">
        <el-select v-model="model.parent">
          <el-option v-for="li in parents"
